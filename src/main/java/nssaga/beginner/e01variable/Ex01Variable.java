package nssaga.beginner.e01variable;

/**
 * A variable is an identifier, which holds data/value and can be changed at run
 * time.
 * 
 * Based on position, behavior and declaration variable categories into 3 parts instance, static, local variable
 */
public class Ex01Variable {
	/*
	 * syntax : data_type <variable_name> = <constant/literal value>;
	 */
	public static void main(String[] args) {
		String name = "Nawal"; // Assigning a value to variable "name"
		System.out.println(name);
	}
}
